import mongoose from "mongoose";

const userSchema = mongoose.Schema(
  {
    name:{
      type:String
    },
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    isAdmin: {
      type: Boolean,
      required: true,
      default: false,
    },
  },
  {
    timestamps: true,
  }
);

const USERS = mongoose.model("user", userSchema);
export default USERS;
